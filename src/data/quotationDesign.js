const BOM_CATEGORY_ORDER = [
  "Solar Modules",
  "Inverter",
  "Battery",
  "Mounting Structure",
  "Electrical Components",
  "Safety Components",
  "Accessories",
];

function bomCategory(item) {
  const d = item.description.toLowerCase();
  if (d.includes("solar module") || (d.includes("module") && !d.includes("inverter"))) return "Solar Modules";
  if (d.includes("inverter")) return "Inverter";
  if (d.includes("battery")) return "Battery";
  if (
    d.includes("structure") ||
    d.includes("apollo") ||
    d.includes("clamp") ||
    d.includes("base plate") ||
    d.includes("perlin") ||
    d.includes("z clamp") ||
    d.includes("mid clamp")
  )
    return "Mounting Structure";
  if (d.includes("earthing") || d.includes("la-cb") || d.includes("lightning")) return "Safety Components";
  if (
    d.includes("cable") ||
    d.includes("acdb") ||
    d.includes("dcdb") ||
    d.includes("mc4") ||
    d.includes("havells") ||
    d.includes("polycab") ||
    d.includes("waacab") ||
    d.includes("pvc") ||
    d.includes("pipe") ||
    d.includes("lug") ||
    d.includes("channel")
  )
    return "Electrical Components";
  return "Accessories";
}

/** Group BOM rows for categorized table — all items preserved, order within category kept. */
export function groupBomByCategory(bom) {
  const map = new Map();
  for (const item of bom) {
    const cat = bomCategory(item);
    if (!map.has(cat)) map.set(cat, []);
    map.get(cat).push(item);
  }
  return BOM_CATEGORY_ORDER.filter((c) => map.has(c)).map((category) => ({
    category,
    items: map.get(category),
  }));
}

/** Estimated savings KPIs — placeholder architecture when live data unavailable. */
export function estimateProjectKpis(kwp) {
  const kw = Number(kwp) || 3.5;
  const annualGenKwh = Math.round(kw * 4.5 * 365);
  const monthlySavings = Math.round((annualGenKwh / 12) * 6);
  const co2Kg = Math.round(annualGenKwh * 0.82);
  const savings25Year = monthlySavings * 12 * 25;
  const roiYears = Math.max(3, Math.round((kw * 58000) / (monthlySavings * 12)));

  return {
    monthlySavings,
    annualGeneration: `${annualGenKwh.toLocaleString("en-IN")} kWh`,
    savings25Year,
    co2Reduction: `${co2Kg.toLocaleString("en-IN")} kg`,
    roiPeriod: `${roiYears}–${roiYears + 1} Years`,
  };
}

export function panelKpiLabel(matched) {
  if (!matched?.panel) return "—";
  return `${matched.panel.panelCount} × ${matched.panel.wattPerPanel}W`;
}

export function inverterKpiLabel(matched) {
  if (!matched?.inverter) return "—";
  return `${matched.inverter.capacityKw}kW ${matched.inverter.brand}`;
}

